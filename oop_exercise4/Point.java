/**
 * A class creating a 3D point and ways of manipulating it
 * @author ccheibas
 *
 */

public class Point {
	private int x;
	private int y;
	private int z;
	public void setX(int var) {
		this.x = var;
	}
	public void setY(int var) {
		this.y = var;
	}
	public void setZ(int var) {
		this.z = var;
	}
	public int getX() {
		return this.x;
	}
	public int getY() {
		return this.y;
	}
	public int getZ() {
		return this.z;
	}


}
